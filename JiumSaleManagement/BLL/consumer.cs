		public List<Jium.Model.consumer> GetModelList(string strWhere)
		{
			DataSet ds = dal.GetList(strWhere);
			return DataTableToList(ds.Tables[0]);
		}
		/// <summary>
		/// 获得数据列表
		/// </summary>
		public List<Jium.Model.consumer> DataTableToList(DataTable dt)
		{
			List<Jium.Model.consumer> modelList = new List<Jium.Model.consumer>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				Jium.Model.consumer model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

